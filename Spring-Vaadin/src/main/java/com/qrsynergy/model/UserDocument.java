package com.qrsynergy.model;

public class UserDocument {

    // visible name to the user
    private String name;

    // url of the qr
    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
